public abstract  class Person {
    public Person(String head, String body, String lefthands, String righthads, String leftleg, String rightleg) {
        this.head = head;
        this.body = body;
        this.lefthands = lefthands;
        this.righthads = righthads;
        this.leftleg = leftleg;
        this.rightleg = rightleg;
    }

    public String head;
    public String body;
    public String lefthands;
    public String righthads;
    public String leftleg;
    public String rightleg;


}
